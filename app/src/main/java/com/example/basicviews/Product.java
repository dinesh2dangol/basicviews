package com.example.basicviews;

public class Product {

    private int id;
    private String title;
    private String description;
    private String price;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }

    public String getPrice() {
        return price;
    }
}

